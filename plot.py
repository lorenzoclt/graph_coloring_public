import pandas as pd
import re
import matplotlib.pyplot as plt

def parse_data(text):
    rows = []
    for line in text.strip().split('\n'):
        # Split the data by " ==> " and by "-".
        prefix, metrics = line.split(' ==> ')
        metrics_dict = {}
        
        # Parse metrics part
        for metric in re.split('-(?=[A-Z])', metrics):
            key_val = metric.split('_')
            if len(key_val) == 3 and key_val[0] == 'E':
                metrics_dict['E_mean'] = key_val[1]
                metrics_dict['E_std'] = key_val[2]
            elif len(key_val) == 2:
                k, v = key_val
                metrics_dict[k] = v
            elif len(key_val) > 2:
                k = key_val[0]
                v = '_'.join(key_val[1:])
                metrics_dict[k] = v
            else:
                k = key_val[0]
                metrics_dict[k] = None
        
        # Parsing prefix to get the data type and specific values
        prefix_parts = prefix.split('/')
        metrics_dict['type'] = prefix_parts[2]
        
        for part in prefix_parts[3:]:
            key_val = part.split('_')
            if len(key_val) == 2:
                key, val = key_val
                if key in ['G', 'N', 'E']:
                    metrics_dict[key] = int(val)
                elif key == 'C':
                    metrics_dict[key] = float(val)
        
        # Converting values to appropriate types
        for key in metrics_dict:
            if metrics_dict[key] is not None:
                try:
                    if '.' in metrics_dict[key]:
                        metrics_dict[key] = float(metrics_dict[key])
                    else:
                        metrics_dict[key] = int(metrics_dict[key])
                except ValueError:
                    pass  # Keep as string if conversion fails
        
        rows.append(metrics_dict)
    
    return pd.DataFrame(rows)

def plot_data(df, x_col, y_col, plot_type='scatter', title=None, error_col=None, color_by=None):
    plt.figure(figsize=(10, 6))
    
    # Group by the specified columns and plot each group with a unique color
    groups = df.groupby(color_by)
    for name, group in groups:
        if plot_type == 'scatter':
            if error_col:
                plt.errorbar(group[x_col], group[y_col], yerr=group[error_col], fmt='o', label=name)
            else:
                plt.scatter(group[x_col], group[y_col], label=name)
        elif plot_type == 'line':
            if error_col:
                plt.errorbar(group[x_col], group[y_col], yerr=group[error_col], fmt='-', label=name)
            else:
                plt.plot(group[x_col], group[y_col], label=name)
        elif plot_type == 'bar':
            if error_col:
                plt.bar(group[x_col], group[y_col], yerr=group[error_col], capsize=5, label=name)
            else:
                plt.bar(group[x_col], group[y_col], label=name)
        else:
            raise ValueError(f"Unsupported plot_type: {plot_type}")
    
    plt.xlabel(x_col)
    plt.ylabel(y_col)
    if title:
        plt.title(title)
    if color_by:
        plt.legend(title=color_by)
    plt.savefig('plot.png')

